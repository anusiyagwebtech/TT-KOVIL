சாலிச்சந்தையில் பால்பழக்காரி அம்மனை 1918ம் ஆண்டில் கும்பிட்ட பின்பு முதன் முதலாக 1960ம் ஆண்டு கும்பிடப்பட்டது. அதற்குப்பிறகு ஒவ்வொரு ஆண்டும் மாசி மாக சிவராத்திரிக்குப் பிறகு வருகின்ற வளர்பிறையில் வரும் ஒரு வெள்ளிக்கிழமை அன்று கும்பிட்டு வருகின்றோம். சாலிச்சிந்தையில் நம் குலதெய்வம் பால்பழக்காரி அம்மன் ஸ்ரீ சீ.<br><br>பரங்கிரி நாடார் அவர்கள் வீட்டில் வீற்றிருந்த பின்பு 1962ல் பால்பழக்காரி அம்மனுக்கு நம் புளியந்தோப்பில் கருணையானந்த சுவாமி கோவிலுக்கு தெற்கிலும். மாரியம்மன் கோவிலுக்கு மேற்கிலும் 100- 7.2 இடத்தில் கோவில் திருப்பணி. மூலஸ்தானம், திருமதில் முதலானவை கட்டுவதற்கு தீர்மானிக்கப்ட்டு கட்டிட வேலைகளை நடத்தி பால்பழக்காரி அம்மனையும் புதுக்கோவில் பிரதிஷ்டை செய்து வைக்கப்பட்டது. 
திரு.S.V.K.S. சோமசுந்தர நாடார் உசிலம்பட்டி அவர்கள் அம்மன் சிலையும் உசிலை திரு. K.A.சந்தோஷ் நாடார் கோயில் கதவினையும், உசிலை திரு. S.P.கருணையனந்தம், குமாரர்கள் வடக்கு வாசல் இரும்புக் கதவினையும் அமைத்துக் கொடுத்தனர், 
கோவில் முன்மண்டபம் சுமார் 1.75 லட்சம் செலவில் கட்டி முடிக்கப்பட்டது.<br><br> ஒவ்வொரு வருடமும் நமது பங்காளிகள் அதிக அளவில் வந்திருந்து சாமிகும்பிட்டு அம்மனின் அருள் பெற்று வருகிறோம். நமது வீட்டில் பிறந்த பெண் குழந்தைகளும் திருமணத்திற்குப் பிறகு குடும்பத்துடன் வந்து சாமி கும்பிடுவது அதிகமாகி வருகிறது. இடையில் சில திருடர்களால் மூல ஸ்தானத்திலுள்ள அம்மன் சிலை உடைக்கப்பட்டது. பின்னர் ஆகம விதிகளுடன் புதிய அம்மன் சிலை செய்து பிரதிஷ்டை செய்யப்பட்டள்ளது. 
