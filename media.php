  <p>நமது குலதெய்மாகிய ஸ்ரீ குமரவிடங்க அய்யனார் திருக்கோவில் தூத்துக்குடி மாவட்டம் திருச்செந்தூர் வட்டம், சுகந்தலை ஊராட்சி, மைக்கண் நாடான் குடியிருப்பு கிராமத்தில் அமைந்துள்ளது. இக்கோவில் திருநேல்வேலி திருச்செந்தூர் மார்க்கத்தில் உள்ள குரும்பூரில் இருந்து சுமார் 2 கி.மீ தூரத்திலும், தூத்துக்குடி - திருச்செந்தூர் மாக்கத்தில் ஆத்தூருக்கு அடுத்தாற்போல் பேருந்துகள் ஐயன் கோவில் என்ற நிறுத்தத்தில் இருந்து 2 கி.மீ தூரத்திலும், தூத்துக்குடி - திருச்செந்தூா மார்க்கத்தில் ஆத்தூருக்கு அடுத்தாற் போல் பேருந்துக்ள ஐயன் கோவில் என்ற நிறுத்தத்தில் இருந்து 2 கி.மீ தொலைவில் நமது கோவில் அமைந்துள்ளது. தூத்துக்குடியில் இருந்து ஏரல் செல்லும் பேருந்துகள் சில நமது கோவில் வழியாகச் செல்கிறது. 
நமது குலதெய்வம் அருள்மிகு குமரவிடங்க அய்யனார் திருத்தலம் சுமார் 500 ஆண்டுகளுக்கு முந்தைய ஸ்தலம் என்று கிராமத்து பெரியவர்கள் கூறுகிறார்கள். நமது முன்னோர்கள் இந்தக் கோவில் வந்து சாமி கும்பிட்டு, சில காரியங்கள் செய்ய இந்தக் கோவிலில் உத்தரவு கேட்டு அதன்படி செய்திருக்கிறார்கள் என்பதனை நமது பங்காளிகள் வரலாற்றில் காணலாம்.<br><br> 1918க்குப் பிறகு நமது முன்னோர்கள் குலதெய்வம் கும்பிடுவதை நிறுத்திவிட்டார்கள். சுமார் 50 வருடங்களுக்கு முன்னர் நமது முன்னோர்கள் தம் பெரியவர்களின் வழிகாட்டுதலின் பேரில் இந்த ஸ்தலத்தைக் கண்டுபிடித்தார்கள். அந்த நேரத்தில் பயங்கரமான உடைமரம், பாம்பு புற்றுகளுக்கு நடுவில் இப்போது இருக்கின்ற அதே நிலையில் பீடம் இருந்ததாகவும், அதை சுத்தம் செய்து பீடத்திற்குள் ஓர் ஆள் செல்கின்ற அளவிற்கு செப்பனிட்டதாவும் சொல்லப்படுகிறது. நாளாவட்டத்தில் இந்தக் கோவிலின் சுற்றுப் புறங்களைச் சுத்தம் செய்து. இன்று காணப்படுகிற கோட்டை மதிலை, தூத்துக்கடியைச் சேர்ந்த திரு.அ.சிவாபாண்டி நாடார் தலைமையில் உள்ளூர் பிரமுகர்கள் தங்கப்பாண்டி நாடார், பாண்டிபழ நாடார் அவர்கள் உதவியுடன் கட்டியிருக்கலாம் என்று கூறப்படுகிறது. 
அதன்பிறகு சங்கிலி கருப்பன் சிலை நிறுவப்பட்டது. உசிலம்பட்டி அய்ய நாடார் வாரிசுகள் பைரவர்கள் சிலையை நிறுவினார்கள். பீடத்தின் முன்னால் சுவாமி கும்பிடுவதற்கு வசதியாக கூரை போடப்பட்டுள்ளது. கோவிலில் சிவராத்திரி அன்று ஒவ்வொரு வருடமும் சாமி கும்பிடும் போது நமது பங்காளிகள் தங்குவதற்காக வசதி செய்யப்பட்டுள்ளது. 
இக்கோவிலுக்கு கும்பாபிஷேகம் நடத்த திட்டமிட்டபோது பீடத்தில் எந்த ஒரு மாறுதலும் செய்ய சுவாமி உத்தரவு கிடைக்கவில்லை, கும்பாபிஷேகத்தை முன்னிட்டு மண் கலசத்தை மாறி செம்பு கலசம் போடவும் சுவாமி உத்தரவு கிடைக்கவில்லை. 
5.6.1999 அன்று கோவில் கும்பாமிஷேகம் நடைபெற்றது. 12 ஆண்டுகள் கழித்து 9.2.11 புதன்கிழமை அன்று கும்பாபிஷேகம் நடைபெற்றது.<br><br> அய்யன் கோயிலை புனரமைப்பு செய்து புதியனவாகக் கட்டுவதற்கு அய்யன் உத்தரவு கொடுத்ததின் பேரில் நமது பங்காளிகளும், உள்ளூர் மக்களும் மற்றோரும் சேர்ந்து புதிய கோயிலை ஸதாபித்து திருக்கோயில் அழகுற கம்பீரத்துடன் இருக்கிறது. 
சுற்றுவட்டாரத்தில் உள்ள அய்யனார் சுவாமிகளுக்கு மாறுபட்டு ஸ்ரீ குமர விடங்க அய்யனார் முனிவர் தவக் கோலத்தில் காட்சி அளிக்கிறார். திருச்சிந்தூர் கோயிலிலும் ஸ்ரீ குமார விடங்க அய்யன் காவல் தெய்வமாக வீற்றிருக்கின்றார். </p>    
